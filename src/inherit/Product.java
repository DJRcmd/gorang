package inherit;

public class Product {
    private int code;
    private String name;
    private int price;
    public int getCode() {
        return code;
    }
    public void setCode(int code){
        this.code = code;
    }
    public String getName() {
        return name;
    }
    public String setName(String name){
        return name;
    }
    public int getPrice() {
        return price;
    }
    public void setPrice(int price){
        this.price = price;
    }

}
